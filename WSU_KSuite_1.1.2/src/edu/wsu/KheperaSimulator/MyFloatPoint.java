package edu.wsu.KheperaSimulator;
/**
 * A generic class to represent a 2-dimensional point in float.
 *
 */
public class MyFloatPoint {
    /** */
    protected float x;

    /** */
    protected float y;

    /**
     * Allocate a new <code>MyPoint</code> object with specified coordinate parameters.
     */
    public MyFloatPoint(float x, float y) {
      this.x = x;
      this.y = y;
    }
} // MyPoint
